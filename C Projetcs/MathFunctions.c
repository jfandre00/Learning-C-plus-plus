#include <stdio.h>
#include <math.h> // math library

int main() {

    double A = sqrt(9);
    printf("%f\n", A);

    double B = pow(2, 4);
    printf("%f\n", B);

    int C = round(3.14);
    printf("%d\n", C);

    int D = ceil(3.14); // round up
    printf("%d\n", D);

    int E = floor(3.14); // round down
    printf("%d\n", E);

    double F = fabs(-100);
    printf("%f\n", F);

    double G = log(3);
    printf("%f\n", G);

    double H = sin(45);
    printf("%f\n", H);

    double I = cos(45);
    printf("%f\n", I);

    double J = tan(45);
    printf("%f\n", J);



    return 0;
}